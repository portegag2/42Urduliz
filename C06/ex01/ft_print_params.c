#include <unistd.h>

int	main(int argc, char **argv)
{
	if (argc > 1)
	{
		while (argc-- > 1 && **(++argv))
		{
			while (**argv)
				write(1, (*(argv))++, 1);
			write (1, "\n", 1);
		}
	}
}
